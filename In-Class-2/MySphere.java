public class MySphere {
    public static double sphereVolume3( double radius )
    {
       double volume = ( 4.0 / 3.0 ) * Math.PI * Math.pow( radius, 3 );
       return volume;
    }

    public double sphereVolume2( double radius )
    {
       double volume = ( 4.0 / 3.0 ) * Math.PI * Math.pow( radius, 3 );
       return volume;
    }
}
