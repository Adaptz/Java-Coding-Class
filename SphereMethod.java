public class SphereMethod {
    public static double sphereVolume2( double radius )
    {
       double volume = ( 4.0 / 3.0 ) * Math.PI * Math.pow( radius, 3 );
       return volume;
    } // end method sphereVolume
    public double sphereVolume3( double radius )
    {
       double volume = ( 4.0 / 3.0 ) * Math.PI * Math.pow( radius, 3 );
       return volume;
    } // end method sphereVolume
}
